"""
this class defines a particular interaction
the key is (x,y) where x and y are the bin number (x < y)
"""
class Interaction(object):
    def __init__(self, cc, pval, qval):
        self.contact = cc
        self.PValue = pval
        self.QValue = qval

    def _GetCC(self):
        return self.contact

    def _GetPVal(self):
        return self.PValue

    def _GetQVal(self, count=1):
        return self.QValue
